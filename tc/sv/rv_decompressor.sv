module rv_decompressor #(
	parameter rv64 = 1
) (
	input bit[31:0] in,

	output bit sigill,
	output logic is_compressed,
	output logic[31:0] out
);
	typedef enum bit[4:0] {
		OpCode_Load = 5'b00000,
		OpCode_LoadFp = 5'b00001,
		OpCode_OpImm = 5'b00100,
		OpCode_OpImm32 = 5'b00110,
		OpCode_Store = 5'b01000,
		OpCode_StoreFp = 5'b01001,
		OpCode_Op = 5'b01100,
		OpCode_Lui = 5'b01101,
		OpCode_Op32 = 5'b01110,
		OpCode_Branch = 5'b11000,
		OpCode_Jalr = 5'b11001,
		OpCode_Jal = 5'b11011,
		OpCode_System = 5'b11100
	} OpCode;

	`define type_r(result, opcode, rd, funct3, rs1, rs2, funct7) begin \
		result[0+:2] = 2'b11; \
		result[2+:5] = opcode; \
		result[7+:5] = rd; \
		result[12+:3] = funct3; \
		result[15+:5] = rs1; \
		result[20+:5] = rs2; \
		result[25+:7] = funct7; \
	end

	`define type_i(result, opcode, rd, funct3, rs1, imm) begin \
		result[0+:2] = 2'b11; \
		result[2+:5] = opcode; \
		result[7+:5] = rd; \
		result[12+:3] = funct3; \
		result[15+:5] = rs1; \
		result[20+:12] = imm; \
	end

	`define type_s(result, opcode, funct3, rs1, rs2, imm) begin \
		result[0+:2] = 2'b11; \
		result[2+:5] = opcode; \
		result[12+:3] = funct3; \
		result[15+:5] = rs1; \
		result[20+:5] = rs2; \
		{result[25+:7], result[7+:5]} = imm; \
	end

	`define type_b(result, opcode, funct3, rs1, rs2, imm) begin \
		result[0+:2] = 2'b11; \
		result[2+:5] = opcode; \
		result[12+:3] = funct3; \
		result[15+:5] = rs1; \
		result[20+:5] = rs2; \
		{result[31], result[7], result[25+:6], result[8+:4]} = imm; \
	end

	`define type_u(result, opcode, rd, imm) begin \
		result[0+:2] = 2'b11; \
		result[2+:5] = opcode; \
		result[7+:5] = rd; \
		result[12+:20] = imm; \
	end

	`define type_j(result, opcode, rd, imm) begin \
		result[0+:2] = 2'b11; \
		result[2+:5] = opcode; \
		result[7+:5] = rd; \
		{result[31], result[12+:8], result[20], result[21+:10]} = imm; \
	end

	always_comb begin
		sigill = '0;
		is_compressed = '1;
		out = 'x;

		unique case (in[0+:2])
			2'b00: unique case (in[13+:3])
				3'b000: if (in[2+:11] == '0) begin
					sigill = '1;
					is_compressed = 'x;
					out = 'x;
				end else
					// addi4spn
					`type_i(out, OpCode_OpImm, {2'b01, in[2+:3]}, 3'b000, 5'b00010, {2'b00, in[7+:4], in[11+:2], in[5], in[6], 2'b00})

				// fld
				3'b001: `type_i(out, OpCode_LoadFp, {2'b01, in[2+:3]}, 3'b011, {2'b01, in[7+:3]}, {4'b0000, in[5+:2], in[10+:3], 3'b000})

				// lw
				3'b010: `type_i(out, OpCode_Load, {2'b01, in[2+:3]}, 3'b010, {2'b01, in[7+:3]}, {5'b00000, in[5], in[10+:3], in[6], 2'b00})

				3'b011: if (rv64)
					// ld
					`type_i(out, OpCode_Load, {2'b01, in[2+:3]}, 3'b011, {2'b01, in[7+:3]}, {4'b0000, in[5+:2], in[10+:3], 3'b000})
				else
					// flw
					`type_i(out, OpCode_LoadFp, {2'b01, in[2+:3]}, 3'b010, {2'b01, in[7+:3]}, {5'b00000, in[5], in[10+:3], in[6], 2'b00})

				3'b100: unique case ({in[10+:3]})
					// lbu
					3'b000: `type_i(out, OpCode_Load, {2'b01, in[2+:3]}, 3'b100, {2'b01, in[7+:3]}, {10'b0000000000, in[5], in[6]})

					// lhu / lh
					3'b001: `type_i(out, OpCode_Load, {2'b01, in[2+:3]}, {!in[6], 2'b01}, {2'b01, in[7+:3]}, {10'b0000000000, in[5], 1'b0})

					// sb
					3'b010: `type_s(out, OpCode_Store, 3'b000, {2'b01, in[7+:3]}, {2'b01, in[2+:3]}, {10'b000000000, in[5], in[6]})

					// sh
					3'b011: `type_s(out, OpCode_Store, 3'b001, {2'b01, in[7+:3]}, {2'b01, in[2+:3]}, {10'b0000000000, in[5], 1'b0})

					default: begin
						sigill = '1;
						is_compressed = 'x;
					end
				endcase

				// fsd
				3'b101: `type_s(out, OpCode_StoreFp, 3'b011, {2'b01, in[7+:3]}, {2'b01, in[2+:3]}, {4'b0000, in[5+:2], in[10+:3], 3'b000})

				// sw
				3'b110: `type_s(out, OpCode_Store, 3'b010, {2'b01, in[7+:3]}, {2'b01, in[2+:3]}, {5'b00000, in[5], in[10+:3], in[6], 2'b00})

				3'b111: if (rv64)
					// sd
					`type_s(out, OpCode_Store, 3'b011, {2'b01, in[7+:3]}, {2'b01, in[2+:3]}, {4'b0000, in[5+:2], in[10+:3], 3'b000})
				else
					// fsw
					`type_s(out, OpCode_StoreFp, 3'b010, {2'b01, in[7+:3]}, {2'b01, in[2+:3]}, {5'b00000, in[5], in[10+:3], in[6], 2'b00})
			endcase

			2'b01: unique case (in[13+:3])
				// addi
				3'b000: `type_i(out, OpCode_OpImm, in[7+:5], 3'b000, in[7+:5], {{7{in[12]}}, in[2+:5]})

				3'b001: if (rv64)
					// addiw
					`type_i(out, OpCode_OpImm32, in[7+:5], 3'b000, in[7+:5], {{7{in[12]}}, in[2+:5]})
				else
					// jal
					`type_j(out, OpCode_Jal, 5'b00001, {{10{in[12]}}, in[8], in[9+:2], in[6], in[7], in[2], in[11], in[3+:3]})

				// li
				3'b010: `type_i(out, OpCode_OpImm, in[7+:5], 3'b000, 5'b00000, {{7{in[12]}}, in[2+:5]})

				3'b011: if (in[7+:5] == 5'b00010)
					// addi16sp
					`type_i(out, OpCode_OpImm, 5'b00010, 3'b000, 5'b00010, {{3{in[12]}}, in[3+:2], in[5], in[2], in[6], 4'b0000})
				else
					// lui
					`type_u(out, OpCode_Lui, in[7+:5], {{15{in[12]}}, in[2+:5]})

				3'b100: unique casez (in[10+:3])
					// srli
					3'b?00: `type_i(out, OpCode_OpImm, {2'b01, in[7+:3]}, 3'b101, {2'b01, in[7+:3]}, {6'b000000, in[12], in[2+:5]})

					// srai
					3'b?01: `type_i(out, OpCode_OpImm, {2'b01, in[7+:3]}, 3'b101, {2'b01, in[7+:3]}, {6'b010000, in[12], in[2+:5]})

					// andi
					3'b?10: `type_i(out, OpCode_OpImm, {2'b01, in[7+:3]}, 3'b111, {2'b01, in[7+:3]}, {{7{in[12]}}, in[2+:5]})

					3'b011: unique case (in[5+:2])
						// sub
						2'b00: `type_r(out, OpCode_Op, {2'b01, in[7+:3]}, 3'b000, {2'b01, in[7+:3]}, {2'b01, in[2+:3]}, 7'b0100000)

						// xor
						2'b01: `type_r(out, OpCode_Op, {2'b01, in[7+:3]}, 3'b100, {2'b01, in[7+:3]}, {2'b01, in[2+:3]}, 7'b0000000)

						// or
						2'b10: `type_r(out, OpCode_Op, {2'b01, in[7+:3]}, 3'b110, {2'b01, in[7+:3]}, {2'b01, in[2+:3]}, 7'b0000000)

						// and
						2'b11: `type_r(out, OpCode_Op, {2'b01, in[7+:3]}, 3'b111, {2'b01, in[7+:3]}, {2'b01, in[2+:3]}, 7'b0000000)
					endcase

					3'b111: unique case (in[5+:2])
						2'b00: if (rv64)
							// subw
							`type_r(out, OpCode_Op32, {2'b01, in[7+:3]}, 3'b000, {2'b01, in[7+:3]}, {2'b01, in[2+:3]}, 7'b0100000)
						else begin
							sigill = '1;
							is_compressed = 'x;
						end

						2'b01: if (rv64)
							// addw
							`type_r(out, OpCode_Op32, {2'b01, in[7+:3]}, 3'b000, {2'b01, in[7+:3]}, {2'b01, in[2+:3]}, 7'b0000000)
						else begin
							sigill = '1;
							is_compressed = 'x;
						end

						2'b11: unique case (in[2+:3])
							// zext.b
							3'b000: `type_i(out, OpCode_OpImm, {2'b01, in[7+:3]}, 3'b111, {2'b01, in[7+:3]}, 12'b000011111111)

							3'b100: if (rv64)
								// zext.w
								`type_r(out, OpCode_Op32, {2'b01, in[7+:3]}, 3'b000, {2'b01, in[7+:3]}, 5'b00000, 7'b0000100)
							else begin
								sigill = '1;
								is_compressed = 'x;
							end

							// not
							3'b101: `type_i(out, OpCode_OpImm, {2'b01, in[7+:3]}, 3'b100, {2'b01, in[7+:3]}, 12'b111111111111)

							default: begin
								sigill = '1;
								is_compressed = 'x;
							end
						endcase

						default: begin
							sigill = '1;
							is_compressed = 'x;
						end
					endcase

					default: begin
						sigill = '1;
						is_compressed = 'x;
					end
				endcase

				// j
				3'b101: `type_j(out, OpCode_Jal, 5'b00000, {{10{in[12]}}, in[8], in[9+:2], in[6], in[7], in[2], in[11], in[3+:3]})

				// beqz
				3'b110: `type_b(out, OpCode_Branch, 3'b000, {2'b01, in[7+:3]}, 5'b00000, {{5{in[12]}}, in[5+:2], in[2], in[10+:2], in[3+:2]})

				// bnez
				3'b111: `type_b(out, OpCode_Branch, 3'b001, {2'b01, in[7+:3]}, 5'b00000, {{5{in[12]}}, in[5+:2], in[2], in[10+:2], in[3+:2]})
			endcase

			2'b10: unique case (in[13+:3])
				// slli
				3'b000: `type_i(out, OpCode_OpImm, in[7+:5], 3'b001, in[7+:5], {6'b000000, in[12], in[2+:5]})

				// fldsp
				3'b001: `type_i(out, OpCode_LoadFp, in[7+:5], 3'b011, 5'b00010, {3'b000, in[2+:3], in[12], in[5+:2], 3'b000})

				// lwsp
				3'b010: `type_i(out, OpCode_Load, in[7+:5], 3'b010, 5'b00010, {4'b0000, in[2+:2], in[12], in[4+:3], 2'b00})

				3'b011: if (rv64)
					// ldsp
					`type_i(out, OpCode_Load, in[7+:5], 3'b011, 5'b00010, {3'b000, in[2+:3], in[12], in[5+:2], 3'b000})
				else
					// flwsp
					`type_i(out, OpCode_LoadFp, in[7+:5], 3'b010, 5'b00010, {4'b0000, in[2+:2], in[12], in[4+:3], 2'b00})

				3'b100: unique case ({in[7] | in[8] | in[9] | in[10] | in[11], in[2] | in[3] | in[4] | in[5] | in[6]})
					2'b00: if (in[12])
						// ebreak
						`type_r(out, OpCode_System, 5'b00000, 3'b000, 5'b00000, 5'b00001, 7'b0000000)
					else begin
						sigill = '1;
						is_compressed = 'x;
					end

					// jr, jalr
					2'b10: `type_i(out, OpCode_Jalr, {4'b0000, in[12]}, 3'b000, in[7+:5], 12'b000000000000)

					// mv, add
					2'b11: `type_r(out, OpCode_Op, in[7+:5], 3'b000, {5{in[12]}} & in[7+:5], in[2+:5], 7'b0000000)

					default: begin
						sigill = '1;
						is_compressed = 'x;
					end
				endcase

				// fsdsp
				3'b101: `type_s(out, OpCode_StoreFp, 3'b011, 5'b00010, in[2+:5], {3'b000, in[7+:3], in[10+:3], 3'b000})

				// swsp
				3'b110: `type_s(out, OpCode_Store, 3'b010, 5'b00010, in[2+:5], {4'b0000, in[7+:2], in[9+:4], 2'b00})

				3'b111: if (rv64)
					// sdsp
					`type_s(out, OpCode_Store, 3'b011, 5'b00010, in[2+:5], {3'b000, in[7+:3], in[10+:3], 3'b000})
				else
					// fswsp
					`type_s(out, OpCode_StoreFp, 3'b010, 5'b00010, in[2+:5], {4'b0000, in[7+:2], in[9+:4], 2'b00})
			endcase

			// uncompressed
			2'b11: begin
				is_compressed = '0;
				out = {in[2+:30], 2'b11};
			end
		endcase
	end
endmodule

`ifdef TESTING
`define test_ok(in_, out_) begin \
	in = in_; \
	#1 \
	assert(sigill == '0) else $fatal; \
	assert(is_compressed == '1) else $fatal; \
	assert(out == out_) else $fatal; \
end

`define test_err(in_) begin \
	in = in_; \
	#1 \
	assert(sigill == '1) else $fatal; \
end

module test_rv_decompressor32;
	bit[31:0] in;
	wire sigill;
	wire is_compressed;
	wire[31:0] out;
	rv_decompressor #(.rv64(0)) rv_decompressor_module (
		in,
		sigill, is_compressed, out
	);

	initial begin
		// All zeros
		`test_err(32'b0)

		// addi4spn
		`test_ok(32'b000_01010101_010_00, 32'b000101011000_00010_000_01010_00100_11)

		// fld
