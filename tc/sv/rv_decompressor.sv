module rv_decompressor #(
	parameter rv64 = 1
) (
	input bit[31:0] in,

	output bit sigill,
	output logic is_compressed,
	output logic[31:0] out
);
	always_comb begin
		sigill = '0;
		is_compressed = '1;
		out = 'x;

		unique casez ({in[0+:2], in[13+:3]})
			// addi4spn
			5'b00000: if (in[2+:11] == '0) begin
				sigill = '1;
				is_compressed = 'x;
				out = 'x;
			end else begin
				out[0+:7] = 7'b0010011;
				out[7+:5] = {2'b01, in[2+:3]};
				out[12+:3] = 3'b000;
				out[15+:5] = 5'b00010;
				out[20+:12] = {2'b00, in[7+:4], in[11+:2], in[5], in[6], 2'b00};
			end

			// fld
			5'b00001: begin
				out[0+:7] = 7'b0000111;
				out[7+:5] = {2'b01, in[2+:3]};
				out[12+:3] = 3'b011;
				out[15+:5] = {2'b01, in[7+:3]};
				out[20+:12] = {4'b0000, in[5+:2], in[10+:3], 3'b000};
			end

			// lw
			5'b00010: begin
				out[0+:7] = 7'b0000011;
				out[7+:5] = {2'b01, in[2+:3]};
				out[12+:3] = 3'b010;
				out[15+:5] = {2'b01, in[7+:3]};
				out[20+:12] = {5'b00000, in[5], in[10+:3], in[6], 2'b00};
			end

			5'b00011: if (rv64) begin
				// ld
				out[0+:7] = 7'b0000011;
				out[7+:5] = {2'b01, in[2+:3]};
				out[12+:3] = 3'b011;
				out[15+:5] = {2'b01, in[7+:3]};
				out[20+:12] = {4'b0000, in[5+:2], in[10+:3], 3'b000};
			end else begin
				// flw
				out[0+:7] = 7'b0000111;
				out[7+:5] = {2'b01, in[2+:3]};
				out[12+:3] = 3'b010;
				out[15+:5] = {2'b01, in[7+:3]};
				out[20+:12] = {5'b00000, in[5], in[10+:3], in[6], 2'b00};
			end

			// Zcb
			5'b00100: begin
				sigill = '1;
				is_compressed = 'x;
			end

			// fsd
			5'b00101: begin
				out[0+:7] = 7'b0100111;
				out[12+:3] = 3'b011;
				out[15+:5] = {2'b01, in[7+:3]};
				out[20+:5] = {2'b01, in[2+:3]};
				{out[25+:7], out[7+:5]} = {4'b0000, in[5+:2], in[10+:3], 3'b000};
			end

			// sw
			5'b00110: begin
				out[0+:7] = 7'b0100011;
				out[12+:3] = 3'b010;
				out[15+:5] = {2'b01, in[7+:3]};
				out[20+:5] = {2'b01, in[2+:3]};
				{out[25+:7], out[7+:5]} = {5'b00000, in[5], in[10+:3], in[6], 2'b00};
			end

			5'b00111: if (rv64) begin
				// sd
				out[0+:7] = 7'b0100011;
				out[12+:3] = 3'b011;
				out[15+:5] = {2'b01, in[7+:3]};
				out[20+:5] = {2'b01, in[2+:3]};
				{out[25+:7], out[7+:5]} = {4'b0000, in[5+:2], in[10+:3], 3'b000};
			end else begin
				// fsw
				out[0+:7] = 7'b0100111;
				out[12+:3] = 3'b010;
				out[15+:5] = {2'b01, in[7+:3]};
				out[20+:5] = {2'b01, in[2+:3]};
				{out[25+:7], out[7+:5]} = {5'b00000, in[5], in[10+:3], in[6], 2'b00};
			end

			// addi
			5'b01000: begin
				out[0+:7] = 7'b0010011;
				out[7+:5] = in[7+:5];
				out[12+:3] = 3'b000;
				out[15+:5] = in[7+:5];
				out[20+:12] = {{7{in[12]}}, in[2+:5]};
			end

			5'b01001: if (rv64) begin
				// addiw
				out[0+:7] = 7'b0011011;
				out[7+:5] = in[7+:5];
				out[12+:3] = 3'b000;
				out[15+:5] = in[7+:5];
				out[20+:12] = {{7{in[12]}}, in[2+:5]};
			end else begin
				// jal
				out[0+:7] = 7'b1101111;
				out[7+:5] = 5'b00001;
				{out[31], out[12+:8], out[20], out[21+:10]} = {{10{in[12]}}, in[8], in[9+:2], in[6], in[7], in[2], in[11], in[3+:3]};
			end

			// li
			5'b01010: begin
				out[0+:7] = 7'b0010011;
				out[7+:5] = in[7+:5];
				out[12+:3] = 3'b000;
				out[15+:5] = 5'b00000;
				out[20+:12] = {{7{in[12]}}, in[2+:5]};
			end

			5'b01011: if (in[7+:5] == 5'b00010) begin
				// addi16sp
				out[0+:7] = 7'b0010011;
				out[7+:5] = 5'b00010;
				out[12+:3] = 3'b000;
				out[15+:5] = 5'b00010;
				out[20+:12] = {{3{in[12]}}, in[3+:2], in[5], in[2], in[6], 4'b0000};
			end
			else begin
				// lui
				out[0+:7] = 7'b0110111;
				out[7+:5] = in[7+:5];
				out[12+:20] = {{15{in[12]}}, in[2+:5]};
			end

			// misc-alu
			5'b01100: unique case (in[10+:2])
				// srli
				2'b00: begin
					out[0+:7] = 7'b0010011;
					out[7+:5] = {2'b01, in[7+:3]};
					out[12+:3] = 3'b101;
					out[15+:5] = {2'b01, in[7+:3]};
					out[20+:5] = in[2+:5];
					out[25+:7] = {6'b000000, in[12]};
				end

				// srai
				2'b01: begin
					out[0+:7] = 7'b0010011;
					out[7+:5] = {2'b01, in[7+:3]};
					out[12+:3] = 3'b101;
					out[15+:5] = {2'b01, in[7+:3]};
					out[20+:5] = in[2+:5];
					out[25+:7] = {6'b010000, in[12]};
				end

				// andi
				2'b10: begin
					out[0+:7] = 7'b0010011;
					out[7+:5] = {2'b01, in[7+:3]};
					out[12+:3] = 3'b111;
					out[15+:5] = {2'b01, in[7+:3]};
					out[20+:12] = {{7{in[12]}}, in[2+:5]};
				end

				2'b11: unique case ({in[12], in[5+:2]})
					// sub
					3'b000: begin
						out[0+:7] = 7'b0110011;
						out[7+:5] = {2'b01, in[7+:3]};
						out[12+:3] = 3'b000;
						out[15+:5] = {2'b01, in[7+:3]};
						out[20+:5] = {2'b01, in[2+:3]};
						out[25+:7] = 7'b0100000;
					end

					// xor
					3'b001: begin
						out[0+:7] = 7'b0110011;
						out[7+:5] = {2'b01, in[7+:3]};
						out[12+:3] = 3'b100;
						out[15+:5] = {2'b01, in[7+:3]};
						out[20+:5] = {2'b01, in[2+:3]};
						out[25+:7] = 7'b0000000;
					end

					// or
					3'b010: begin
						out[0+:7] = 7'b0110011;
						out[7+:5] = {2'b01, in[7+:3]};
						out[12+:3] = 3'b110;
						out[15+:5] = {2'b01, in[7+:3]};
						out[20+:5] = {2'b01, in[2+:3]};
						out[25+:7] = 7'b0000000;
					end

					// and
					3'b011: begin
						out[0+:7] = 7'b0110011;
						out[7+:5] = {2'b01, in[7+:3]};
						out[12+:3] = 3'b111;
						out[15+:5] = {2'b01, in[7+:3]};
						out[20+:5] = {2'b01, in[2+:3]};
						out[25+:7] = 7'b0000000;
					end

					// subw
					3'b100: if (rv64) begin
						out[0+:7] = 7'b0111011;
						out[7+:5] = {2'b01, in[7+:3]};
						out[12+:3] = 3'b000;
						out[15+:5] = {2'b01, in[7+:3]};
						out[20+:5] = {2'b01, in[2+:3]};
						out[25+:7] = 7'b0100000;
					end else begin
						sigill = '1;
						is_compressed = 'x;
					end

					// addw
					3'b101: if (rv64) begin
						out[0+:7] = 7'b0111011;
						out[7+:5] = {2'b01, in[7+:3]};
						out[12+:3] = 3'b000;
						out[15+:5] = {2'b01, in[7+:3]};
						out[20+:5] = {2'b01, in[2+:3]};
						out[25+:7] = 7'b0000000;
					end else begin
						sigill = '1;
						is_compressed = 'x;
					end

					default: begin
						sigill = '1;
						is_compressed = 'x;
					end
				endcase
			endcase

			// j
			5'b01101: begin
				out[0+:7] = 7'b1101111;
				out[7+:5] = 5'b00000;
				{out[31], out[12+:8], out[20], out[21+:10]} = {{10{in[12]}}, in[8], in[9+:2], in[6], in[7], in[2], in[11], in[3+:3]};
			end

			// beqz
			5'b01110: begin
				out[0+:7] = 7'b1100011;
				out[12+:3] = 3'b000;
				out[15+:5] = {2'b01, in[7+:3]};
				out[20+:5] = 5'b00000;
				{out[31], out[7], out[25+:6], out[8+:4]} = {{5{in[12]}}, in[5+:2], in[2], in[10+:2], in[3+:2]};
			end

			// bnez
			5'b01111: begin
				out[0+:7] = 7'b1100011;
				out[12+:3] = 3'b001;
				out[15+:5] = {2'b01, in[7+:3]};
				out[20+:5] = 5'b00000;
				{out[31], out[7], out[25+:6], out[8+:4]} = {{5{in[12]}}, in[5+:2], in[2], in[10+:2], in[3+:2]};
			end

			// slli
			5'b10000: begin
				out[0+:7] = 7'b0010011;
				out[7+:5] = in[7+:5];
				out[12+:3] = 3'b001;
				out[15+:5] = in[7+:5];
				out[20+:5] = in[2+:5];
				out[25+:7] = {6'b000000, in[12]};
			end

			// fldsp
			5'b10001: begin
				out[0+:7] = 7'b0000111;
				out[7+:5] = in[7+:5];
				out[12+:3] = 3'b011;
				out[15+:5] = 5'b00010;
				out[20+:12] = {3'b000, in[2+:3], in[12], in[5+:2], 3'b000};
			end

			// lwsp
			5'b10010: begin
				out[0+:7] = 7'b0000011;
				out[7+:5] = in[7+:5];
				out[12+:3] = 3'b010;
				out[15+:5] = 5'b00010;
				out[20+:12] = {4'b0000, in[2+:2], in[12], in[4+:3], 2'b00};
			end

			5'b10011: if (rv64) begin
				// ldsp
				out[0+:7] = 7'b0000011;
				out[7+:5] = in[7+:5];
				out[12+:3] = 3'b011;
				out[15+:5] = 5'b00010;
				out[20+:12] = {3'b000, in[2+:3], in[12], in[5+:2], 3'b000};
			end else begin
				// flwsp
				out[0+:7] = 7'b0000111;
				out[7+:5] = in[7+:5];
				out[12+:3] = 3'b010;
				out[15+:5] = 5'b00010;
				out[20+:12] = {4'b0000, in[2+:2], in[12], in[4+:3], 2'b00};
			end

			5'b10100: unique case ({in[12], (in[7] | in[8] | in[9] | in[10] | in[11]), (in[2] | in[3] | in[4] | in[5] | in[6])})
				// jr
				3'b010: begin
					out[0+:7] = 7'b1100111;
					out[7+:5] = 5'b00000;
					out[12+:3] = 3'b000;
					out[15+:5] = in[7+:5];
					out[20+:12] = 12'b000000000000;
				end

				// mv
				3'b011: begin
					out[0+:7] = 7'b0110011;
					out[7+:5] = in[7+:5];
					out[12+:3] = 3'b000;
					out[15+:5] = 5'b00000;
					out[20+:5] = in[2+:5];
					out[25+:7] = 7'b0000000;
				end

				// ebreak
				3'b100: begin
					out[0+:7] = 7'b1110011;
					out[7+:5] = 5'b00000;
					out[12+:3] = 3'b000;
					out[15+:5] = 5'b00000;
					out[20+:12] = 12'b000000000001;
				end

				// jalr
				3'b110: begin
					out[0+:7] = 7'b1100111;
					out[7+:5] = 5'b00001;
					out[12+:3] = 3'b000;
					out[15+:5] = in[7+:5];
					out[20+:12] = 12'b000000000000;
				end

				// add
				3'b111: begin
					out[0+:7] = 7'b0110011;
					out[7+:5] = in[7+:5];
					out[12+:3] = 3'b000;
					out[15+:5] = in[7+:5];
					out[20+:5] = in[2+:5];
					out[25+:7] = 7'b0000000;
				end

				default: begin
					sigill = '1;
					is_compressed = 'x;
				end
			endcase

			// fsdsp
			5'b10101: begin
				out[0+:7] = 7'b0100111;
				out[12+:3] = 3'b011;
				out[15+:5] = 5'b00010;
				out[20+:5] = in[2+:5];
				{out[25+:7], out[7+:5]} = {3'b000, in[7+:3], in[10+:3], 3'b000};
			end

			// swsp
			5'b10110: begin
				out[0+:7] = 7'b0100011;
				out[12+:3] = 3'b010;
				out[15+:5] = 5'b00010;
