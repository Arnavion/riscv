module rv_decompressor #(
	parameter rv64 = 1
) (
	input bit[31:0] in,

	output bit sigill,
	output logic is_compressed,
	output logic[31:0] out
);
	always_comb begin
		if (in[0+:16] == '0) begin
			sigill = '1;
			is_compressed = 'x;
			out = 'x;

		end else begin
			sigill = '0;
			is_compressed = '1;
			out = 'x;

			unique case ({in[0+:2], in[13+:3]})
				// addi4spn
				5'b00000: out = {2'b00, in[7+:4], in[11+:2], in[5], in[6], 12'b000001000001, in[2+:3], 7'b0010011};

				// fld
				5'b00001: out = {4'b0000, in[5+:2], in[10+:3], 5'b00001, in[7+:3], 5'b01101, in[2+:3], 7'b0000111};

				// lw
				5'b00010: out = {5'b00000, in[5], in[10+:3], in[6], 4'b0001, in[7+:3], 5'b01001, in[2+:3], 7'b0000011};

				// flw / ld
				5'b00011:
					if (rv64)
						// ld
						out = {4'b0000, in[5+:2], in[10+:3], 5'b00001, in[7+:3], 5'b01101, in[2+:3], 7'b0000011};
					else
						// flw
						out = {5'b00000, in[5], in[10+:3], in[6], 4'b0001, in[7+:3], 5'b01001, in[2+:3], 7'b0000111};

				// Zcb
				5'b00100: unique case ({in[10+:3]})
					// lbu
					3'b000: out = {10'b0000000000, in[5], in[6], 2'b01, in[7+:3], 5'b10001, in[2+:3], 7'b0000011};

					// lhu / lh
					3'b001: out = {10'b0000000000, in[5], 3'b001, in[7+:3], !in[6], 4'b0101, in[2+:3], 7'b0000011};

					// sb
					3'b010: out = {9'b000000001, in[2+:3], 2'b01, in[7+:3], 6'b000000, in[5], in[6], 7'b0100011};

					// sh
					3'b011: out = {9'b000000001, in[2+:3], 2'b01, in[7+:3], 6'b001000, in[5], 8'b00100011};

					default: begin
						sigill = '1;
						is_compressed = 'x;
					end
				endcase

				// fsd
				5'b00101: out = {4'b0000, in[5+:2], in[12], 2'b01, in[2+:3], 2'b01, in[7+:3], 3'b011, in[10+:2], 10'b0000100111};

				// sw
				5'b00110: out = {5'b00000, in[5], in[12], 2'b01, in[2+:3], 2'b01, in[7+:3], 3'b010, in[10+:2], in[6], 9'b000100011};

				// fsw / sd
				5'b00111:
					if (rv64)
						// sd
						out = {4'b0000, in[5+:2], in[12], 2'b01, in[2+:3], 2'b01, in[7+:3], 3'b011, in[10+:2], 10'b0000100011};
					else
						// fsw
						out = {5'b00000, in[5], in[12], 2'b01, in[2+:3], 2'b01, in[7+:3], 3'b010, in[10+:2], in[6], 9'b000100111};

				// addi
				5'b01000: out = {{7{in[12]}}, in[2+:5], in[7+:5], 3'b000, in[7+:5], 7'b0010011};

				// jal / addiw
				5'b01001:
					if (rv64)
						// addiw
						out = {{7{in[12]}}, in[2+:5], in[7+:5], 3'b000, in[7+:5], 7'b0011011};
					else
						// jal
						out = {in[12], in[8], in[9+:2], in[6], in[7], in[2], in[11], in[3+:3], {9{in[12]}}, 12'b000011101111};

				// li
				5'b01010: out = {{7{in[12]}}, in[2+:5], 8'b00000000, in[7+:5], 7'b0010011};

				// lui / addi16sp
				5'b01011:
					if (in[7+:5] == 5'b00010)
						// addi16sp
						out = {{3{in[12]}}, in[3+:2], in[5], in[2], in[6], 24'b000000010000000100010011};
					else
						// lui
						out = {{15{in[12]}}, in[2+:5], in[7+:5], 7'b0110111};

				// misc-alu
				5'b01100: unique case (in[10+:2])
					// srli
					2'b00: out = {6'b000000, in[12], in[2+:5], 2'b01, in[7+:3], 5'b10101, in[7+:3], 7'b0010011};

					// srai
					2'b01: out = {6'b010000, in[12], in[2+:5], 2'b01, in[7+:3], 5'b10101, in[7+:3], 7'b0010011};

					// andi
					2'b10: out = {{7{in[12]}}, in[2+:5], 2'b01, in[7+:3], 5'b11101, in[7+:3], 7'b0010011};

					2'b11: unique case ({in[12], in[5+:2]})
						// sub
						3'b000: out = {9'b010000001, in[2+:3], 2'b01, in[7+:3], 5'b00001, in[7+:3], 7'b0110011};

						// xor
						3'b001: out = {9'b000000001, in[2+:3], 2'b01, in[7+:3], 5'b10001, in[7+:3], 7'b0110011};

						// or
						3'b010: out = {9'b000000001, in[2+:3], 2'b01, in[7+:3], 5'b11001, in[7+:3], 7'b0110011};

						// and
						3'b011: out = {9'b000000001, in[2+:3], 2'b01, in[7+:3], 5'b11101, in[7+:3], 7'b0110011};

						// subw
						3'b100: out = {9'b010000001, in[2+:3], 2'b01, in[7+:3], 5'b00001, in[7+:3], 7'b0111011};

						// addw
						3'b101: out = {9'b000000001, in[2+:3], 2'b01, in[7+:3], 5'b00001, in[7+:3], 7'b0111011};

						3'b111: unique case (in[2+:3])
							// zext.b
							3'b000: out = {14'b00001111111101, in[7+:3], 5'b11101, in[7+:3], 7'b0010011};

							// not
							3'b101: out = {14'b11111111111101, in[7+:3], 5'b10001, in[7+:3], 7'b0010011};

							default: begin
								sigill = '1;
								is_compressed = 'x;
							end
						endcase

						default: begin
							sigill = '1;
							is_compressed = 'x;
						end
					endcase
				endcase

				// j
				5'b01101: out = {in[12], in[8], in[9+:2], in[6], in[7], in[2], in[11], in[3+:3], {9{in[12]}}, 12'b000001101111};

				// beqz
				5'b01110: out = {{4{in[12]}}, in[5+:2], in[2], 7'b0000001, in[7+:3], 3'b000, in[10+:2], in[3+:2], in[12], 7'b1100011};

				// bnez
				5'b01111: out = {{4{in[12]}}, in[5+:2], in[2], 7'b0000001, in[7+:3], 3'b001, in[10+:2], in[3+:2], in[12], 7'b1100011};

				// slli
				5'b10000: out = {6'b000000, in[12], in[2+:5], in[7+:5], 3'b001, in[7+:5], 7'b0010011};

				// fldsp
				5'b10001: out = {3'b000, in[4], in[2+:2], in[12], in[5+:2], 11'b00000010011, in[7+:5], 7'b0000111};

				// lwsp
				5'b10010: out = {4'b0000, in[2+:2], in[12], in[4+:3], 10'b0000010010, in[7+:5], 7'b0000011};

				// flwsp / ldsp
				5'b10011:
					if (rv64)
						// ldsp
						out = {3'b000, in[4], in[2+:2], in[12], in[5+:2], 11'b00000010011, in[7+:5], 7'b0000011};
					else
						// flwsp
						out = {4'b0000, in[2+:2], in[12], in[4+:3], 10'b0000010010, in[7+:5], 7'b0000111};

				// jr / jalr / mv / add / ebreak
				5'b10100: unique case ({in[12], (in[7] | in[8] | in[9] | in[10] | in[11]), (in[2] | in[3] | in[4] | in[5] | in[6])})
					// jr
					3'b010: out = {12'b000000000000, in[7+:5], 15'b000000001100111};

					// mv
					3'b011: out = {7'b0000000, in[2+:5], 8'b00000000, in[7+:5], 7'b0110011};

					// ebreak
					3'b100: out = {32'b00000000000100000000000001110011};

					// jalr
					3'b110: out = {12'b000000000000, in[7+:5], 15'b000000011100111};

					// add
					3'b111: out = {7'b0000000, in[2+:5], in[7+:5], 3'b000, in[7+:5], 7'b0110011};

					default: begin
						sigill = '1;
						is_compressed = 'x;
					end
				endcase

				// fsdsp
				5'b10101: out = {3'b000, in[9], in[7+:2], in[12], in[2+:5], 8'b00010011, in[10+:2], 10'b0000100111};

				// swsp
				5'b10110: out = {4'b0000, in[7+:2], in[12], in[2+:5], 8'b00010010, in[9+:3], 9'b000100011};

				// fswsp / sdsp
				5'b10111:
					if (rv64)
						// sdsp
						out = {3'b000, in[9], in[7+:2], in[12], in[2+:5], 8'b00010011, in[10+:2], 10'b0000100011};
					else
						// fswsp
						out = {4'b0000, in[7+:2], in[12], in[2+:5], 8'b00010010, in[9+:3], 9'b000100111};

				// uncompressed
				default: begin
					is_compressed = '0;
					out = in;
				end
			endcase
		end
	end
endmodule
